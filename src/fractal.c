#include <fractinc.h>

void		julia(t_env *e)
{
	e->v.c_r = -0.7 + (e->msx / 100);
	e->v.c_i = 0.27015 + (e->msy / 100);
}

void		uter(t_env *e)
{
	e->v.c_r = e->v.x / e->v.zoom + e->v.x1 + e->mx + (e->msx / 100);
	e->v.c_i = e->v.y / e->v.zoom + e->v.y1 + e->my + (e->msy / 100);
}
