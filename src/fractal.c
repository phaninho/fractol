#include "fractinc.h"

void		julia(t_env *e)
{
	e->v.x1 = -1.6;
	e->v.x2 = 1;
	e->v.y1 = -0.95;
	e->v.y2 = 1.4;
	e->v.c_r = -0.7;
	e->v.c_i = 0.27015;
}

void		mandelbrot(t_env *e)
{
	e->v.x1 = -1.6;
	e->v.x2 = 1;
	e->v.y1 = -0.95;
	e->v.y2 = 1.4;
	e->v.c_r = e->v.x / e->v.zoom + e->v.x1 + e->mx;
	e->v.c_i = e->v.y / e->v.zoom + e->v.y1 + e->my;
}
